package it.gopalakrishnan;

import java.util.ArrayList;

public class root {
    private String annoDiInizio;
    classe aula;
    String classe;
    String numeroFinestre;
    String specializzazione;
    ArrayList<studente> studenti;

    public String getAnnoDiInizio() {
        return annoDiInizio;
    }

    public classe getAula() {
        return aula;
    }

    public String getClasse() {
        return classe;
    }

    public String getNumeroFinestre() {
        return numeroFinestre;
    }

    public String getSpecializzazione() {
        return specializzazione;
    }

    public ArrayList<studente> getStudenti() {
        return studenti;
    }
    
}
